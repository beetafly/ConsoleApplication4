#include <iostream>
#include <climits> // Для использования констант типа INT_MAX и INT_MIN
using namespace std;

void findMinMax(int array[], int size, int& minElement, int& maxElement) {
    minElement = INT_MAX; // Инициализируем минимальный элемент максимальным значением типа int
    maxElement = INT_MIN; // Инициализируем максимальный элемент минимальным значением типа int

    // Проходим по каждому элементу массива
    for (int i = 0; i < size; ++i) {
        // Если текущий элемент меньше текущего минимального, обновляем минимальный элемент
        if (array[i] < minElement) {
            minElement = array[i];
        }
        // Если текущий элемент больше текущего максимального, обновляем максимальный элемент
        if (array[i] > maxElement) {
            maxElement = array[i];
        }
    }
}

int main() {
    int array[] = { 3, 8, 2, 5, 1, 7, 4, 9, 6 };
    int size = sizeof(array) / sizeof(array[0]);
    int minElement, maxElement;

    // Находим минимальный и максимальный элементы
    findMinMax(array, size, minElement, maxElement);

    // Выводим результат
    cout << "Минимальный элемент: " << minElement << endl;
    cout << "Максимальный элемент: " << maxElement << endl;

    return 0;
}

